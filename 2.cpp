//2) Program to demonstrate use of different data types.
#include<iostream>
using namespace std;
int main() {
    int num1 = 5; //int data type
    float num2 = 3.145; //float data type
    string name = "salman SK"; //string
    double num3 = 3.4123; //double data type
    char c = 's'; //char data type
    //printing the values
    cout<<"int data type: "<<num1<<endl;
    cout <<"float data type: "<<num2<<endl;
    cout<<"double data type: "<<num3<<endl;
    cout<<"string data type: "<<name<<endl;
    cout<<"char data type: "<<c;
}
//end of program